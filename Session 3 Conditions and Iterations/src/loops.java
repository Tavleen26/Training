
public class loops {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++){
			for(int j=1;j<=i;j++){
				System.out.print(""+j);
			}
			System.out.println("");
		}

	
	for(int r=1;r<=5;r++){
		for(int s=1;s<=r;s++){
			System.out.print(""+r);
		}
		System.out.println("");
	}
	for(int i=1;i<=5;i++){
		for(int j=1;j<=i;j++){
			System.out.print(""+j%2);
		}
		System.out.println("");
	}
		}

	}


